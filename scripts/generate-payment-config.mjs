#!/usr/bin/env node
// Generates/updates paymentMethodConfiguration.js from the org's active payment methods.
//
// Usage:
//   node scripts/generate-payment-config.mjs --org <orgAlias>
//   npm run generate:config -- --org <orgAlias>
//
// Requires: Salesforce CLI (sf) installed and authenticated to the target org.
// The `target` field is optional — leave it empty to use the processor's default merchant account.

import { execSync } from 'child_process';
import { writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';

const __dirname = dirname(fileURLToPath(import.meta.url));

const orgIdx = process.argv.indexOf('--org');
const orgFlag = orgIdx !== -1 ? `--target-org ${process.argv[orgIdx + 1]}` : '';
if (!orgFlag) {
    console.warn('No --org specified — using the default org from sf config.');
}

const apexScript = join(__dirname, 'apex/generate-payment-method-config.apex');
console.log(`Fetching payment methods from org${orgFlag ? ` (${process.argv[orgIdx + 1]})` : ''}...`);

let sfOutput;
try {
    // Do NOT merge stderr (2>&1) — sf CLI warnings go to stderr and would break JSON parsing
    sfOutput = execSync(`sf apex run -f "${apexScript}" ${orgFlag} --json`, { encoding: 'utf8' });
} catch (err) {
    sfOutput = err.stdout ?? '';
    if (!sfOutput) { console.error('sf CLI error:', err.message); process.exit(1); }
}

let parsed;
try {
    parsed = JSON.parse(sfOutput);
} catch {
    // Fallback: extract the JSON object in case there is any non-JSON prefix in stdout
    const match = sfOutput.match(/\{[\s\S]*\}/);
    try {
        parsed = match ? JSON.parse(match[0]) : null;
    } catch { parsed = null; }
    if (!parsed) {
        console.error('Could not parse sf CLI output as JSON. Raw stdout:\n', sfOutput);
        process.exit(1);
    }
}

// On success, sf CLI nests the payload under `result`. On a runtime/compile failure it
// nests the same fields under `data` instead and puts a summary in the top-level `message`.
const result = parsed.result ?? parsed.data ?? {};

if (!result.success) {
    console.error('Apex execution failed.');
    console.error('Exception:', result.exceptionMessage ?? parsed.message ?? '(none)');
    console.error('Compile problem:', result.compileProblem || '(none)');
    process.exit(1);
}

const logs = result.logs ?? '';
const debugLine = logs.split('\n').find(l => l.includes('USER_DEBUG') && l.includes('FDPAYCONFIG:'));
if (!debugLine) {
    console.error('FDPAYCONFIG marker not found in debug log.');
    console.error('Make sure the org has FinDock installed with active payment methods.');
    console.error('\nFull log:\n', logs);
    process.exit(1);
}

const jsonStr = debugLine.slice(debugLine.indexOf('FDPAYCONFIG:') + 'FDPAYCONFIG:'.length).trim();
let config;
try {
    config = JSON.parse(jsonStr);
} catch {
    console.error('Failed to parse config JSON from debug output:\n', jsonStr);
    process.exit(1);
}

function formatEntry(entry) {
    const lines = [
        `    {`,
        `        paymentProcessor: '${entry.paymentProcessor}',`,
        `        paymentMethod: '${entry.paymentMethod}',`,
        `        target: '${entry.target}',`,
        `        enabledOneTime: ${entry.enabledOneTime},`,
        `        enabledRecurring: ${entry.enabledRecurring},`,
        `        isDefaultOneTime: ${entry.isDefaultOneTime},`,
        `        isDefaultRecurring: ${entry.isDefaultRecurring},`,
    ];

    const params = entry.parameters;
    if (params && params.length > 0) {
        lines.push(`        displayLabel: '${entry.displayLabel}',`);
        lines.push(`        parameters: [`);
        params.forEach((p, i) => {
            const comma = i < params.length - 1 ? ',' : '';
            const desc = (p.description ?? '').replace(/\\/g, '\\\\').replace(/'/g, "\\'");
            lines.push(`            {`);
            lines.push(`                name: '${p.name}',`);
            lines.push(`                value: '',`);
            lines.push(`                visibleToCustomer: ${p.visibleToCustomer},`);
            lines.push(`                displayLabel: '${p.displayLabel ?? p.name}',`);
            lines.push(`                required: ${p.required ?? false},`);
            lines.push(`                description: '${desc}'`);
            lines.push(`            }${comma}`);
        });
        lines.push(`        ]`);
    } else {
        lines.push(`        displayLabel: '${entry.displayLabel}'`);
    }

    lines.push(`    }`);
    return lines.join('\n');
}

const header = `// Auto-generated by scripts/generate-payment-config.mjs
// Field reference: skills/payment-web-builder/references/payment-method-selector-config.md
// The \`target\` field is optional — leave empty to use the processor's default merchant account.
//
// Localization: \`displayLabel\`, \`redirectInstruction\` and visible parameter \`displayLabel\`s
// are payer-facing. The script emits the org's English names as plain strings. To make them
// follow the site language, replace a value with a Custom Label reference and import the
// registry at the top of this file, e.g.:
//   import {labels} from './paymentFormLabels';
//   ...
//   displayLabel: labels.ec_label_payment_description,
// Omitting a method \`displayLabel\` falls back to \`paymentMethod\` (the API method name).
// See the README's Localization section.
`;

const body = config.map(formatEntry).join(',\n');
const output = `${header}\nexport const PAYMENT_METHOD_CONFIG = [\n${body}\n];\n`;

const outPath = join(__dirname, '../force-app/main/default/lwc/paymentForm/paymentMethodConfiguration.js');
writeFileSync(outPath, output, 'utf8');

console.log(`\nWritten ${config.length} payment method(s) to:\n  ${outPath}`);
